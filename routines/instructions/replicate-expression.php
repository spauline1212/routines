			<h2>Replicate Expression</h2>

<h3>Definition:</h3>
<p>Let <br /><br />
|F|<sub>1</sub><br />
|F|<sub>2</sub><br />
...<br />
|F|<sub>n</sub><br />
<br />be n string expressions on separate lines, and let f(|F|) be a function that creates a string expression that uses |F|, for example, f(|F|) = "Hello, |F|, let us go the mall, |F|."<br /><br />
Then this routine returns:<br /><br />

f(|F|<sub>1</sub>)<br />
f(|F|<sub>2</sub>)<br />
...<br />
f(|F|<sub>n</sub>)
</p>

<br />
<br /><br />

<h3>Example</h3>
<xmp>
Parameter list:

a1
b2
c3


Expression:

<li>|F|</li>


Results:

<li>a1</li>
<li>b2</li>
<li>c3</li>
</xmp>


